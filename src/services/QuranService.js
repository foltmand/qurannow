// API Service for fetching Quran verses
// Using Al-Quran Cloud API (free and no authentication required)
const BASE_URL = 'https://api.alquran.cloud/v1';

export const QuranService = {
  // Get surah details with verses
  async getSurah(surahNumber, edition = 'en.asad') {
    try {
      const response = await fetch(`${BASE_URL}/surah/${surahNumber}/${edition}`);
      const data = await response.json();
      return data.data;
    } catch (error) {
      console.error('Error fetching surah:', error);
      throw error;
    }
  },

  // Get specific verse
  async getVerse(surahNumber, verseNumber, edition = 'en.asad') {
    try {
      const response = await fetch(`${BASE_URL}/ayah/${surahNumber}:${verseNumber}/${edition}`);
      const data = await response.json();
      return data.data;
    } catch (error) {
      console.error('Error fetching verse:', error);
      throw error;
    }
  },

  // Get Arabic text
  async getArabicSurah(surahNumber) {
    try {
      const response = await fetch(`${BASE_URL}/surah/${surahNumber}/ar.alafasy`);
      const data = await response.json();
      return data.data;
    } catch (error) {
      console.error('Error fetching Arabic surah:', error);
      throw error;
    }
  },

  // Get multiple editions (Arabic + Translation)
  async getSurahWithTranslation(surahNumber) {
    try {
      const [arabic, translation] = await Promise.all([
        this.getArabicSurah(surahNumber),
        this.getSurah(surahNumber, 'en.asad')
      ]);

      // Combine Arabic and translation
      const verses = arabic.ayahs.map((arabicVerse, index) => ({
        number: arabicVerse.number,
        verse: arabicVerse.numberInSurah,
        arabic: arabicVerse.text,
        translation: translation.ayahs[index].text
      }));

      return {
        number: arabic.number,
        name: arabic.englishName,
        arabicName: arabic.name,
        verses: verses,
        numberOfAyahs: arabic.numberOfAyahs,
        revelationType: arabic.revelationType
      };
    } catch (error) {
      console.error('Error fetching surah with translation:', error);
      throw error;
    }
  },

  // Search in Quran
  async searchQuran(query, surahNumber = null) {
    try {
      const url = surahNumber 
        ? `${BASE_URL}/search/${query}/${surahNumber}/en.asad`
        : `${BASE_URL}/search/${query}/all/en.asad`;
      
      const response = await fetch(url);
      const data = await response.json();
      return data.data;
    } catch (error) {
      console.error('Error searching Quran:', error);
      throw error;
    }
  }
};

export default QuranService;
